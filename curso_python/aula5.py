# Aprendendo sobre booleanos

# Booleanos
print(True)
print(False)

# Função que mostra o tipo que o Python atribuiu a variável
print(type(True))
print(type(False))

# Operadores de comparação
print (10 == 10) # Sim => True (Verdadeiro)
print (10 == 11) # Não => False (Falso)

print (10 != 10) # Não => False (Falso)
print (10 != 11) # Sim => True (Verdadeiro)