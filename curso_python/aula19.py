"""
 Operadores de comparação (relacionais)
 OP     Significado             Exemplo (True)
 ==     Igualdade               10 == 10
 !=     Diferença               10 != 10
 >      Maior                   10 > 10
 <      Menor                   10 < 10
 >=     Maior ou igual            10 >= 10
 <=     Menor ou igual
"""

maior = 2 > 1
maior_ou_igual = 2 >= 1
menor = 1 < 2
menor_ou_igual = 2 <= 2
igual = 1 == 1
diferente = 2 != 1

print(maior)
print(maior_ou_igual)
print(menor)
print(menor_ou_igual)
print(igual)
print(diferente)
