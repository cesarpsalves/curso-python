"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
String são textos que estão dentro de aspas
"""

# Aspas simples
print('Isso é uma string de aspas simples')

# Aspas duplas
print("Isso é uma string de aspas duplas")

# Aspas duplas com aspas duplas dentro
print("Isso é uma string de aspas duplas \"com aspas duplas\" dentro")

# Exemplo expressões regulares r
print(r"Isso é uma string de aspas duplas \"com aspas duplas\" dentro")
